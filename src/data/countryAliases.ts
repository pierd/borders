// Map of language codes to their specific aliases
const languageAliases: Record<string, Record<string, string[]>> = {
  en: englishAliases,
  pl: polishAliases,
  es: spanishAliases,
};

// Function to get merged aliases for a specific language
export function getCountryAliases(language: string): Record<string, string[]> {
  const specificAliases = languageAliases[language] || englishAliases;
  const merged: Record<string, string[]> = {};

  // Get all country keys from both common and language-specific aliases
  const allCountries = new Set([
    ...Object.keys(commonAliases),
    ...Object.keys(specificAliases),
  ]);

  for (const country of allCountries) {
    const common = commonAliases[country] || [];
    const specific = specificAliases[country] || [];
    merged[country] = [...common, ...specific];
  }

  return merged;
}
